TEST(SpecifyBlockDeclSim, SpecifyBlockDoesNotInterfereBehavioral) {
  SimFixture f;
  auto* design = ElaborateSrc(
      "module t;\n"
      "  logic [7:0] a, b;\n"
      "  specify\n"
      "    pulsestyle_ondetect out1;\n"
      "    showcancelled out2;\n"
      "    (x => y) = 10;\n"
      "  endspecify\n"
      "  initial begin\n"
      "    a = 8'd11;\n"
      "    b = 8'd22;\n"
      "  end\n"
      "endmodule\n",
      f);
  LowerRunAndCheck(f, design, {{"a", 11u}, {"b", 22u}});
}

