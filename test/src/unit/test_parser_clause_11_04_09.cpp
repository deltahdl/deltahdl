TEST(OperatorAndExpressionParsing, ReductionOnParenthesizedExpr) {
  auto r = Parse(
      "module t;\n"
      "  initial x = &(a ^ b);\n"
      "endmodule\n");
  auto* rhs = FirstInitialRHS(r);
  ASSERT_NE(rhs, nullptr);
  EXPECT_EQ(rhs->kind, ExprKind::kUnary);
  EXPECT_EQ(rhs->op, TokenKind::kAmp);
}

