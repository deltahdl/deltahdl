TEST(ParserSection11, ConditionalTernary) {
  auto r = Parse(
      "module t;\n"
      "  initial x = (a > b) ? a : b;\n"
      "endmodule\n");
  auto* rhs = FirstAssignRhs(r);
  ASSERT_NE(rhs, nullptr);
  EXPECT_EQ(rhs->kind, ExprKind::kTernary);
}

