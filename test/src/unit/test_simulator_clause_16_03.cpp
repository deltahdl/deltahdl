// --- §16.3 Default $error when assert fails without else clause ---

TEST(ImmediateAssertSim, AssertFalseNoElseIncrementsFailCount) {
  SimFixture f;
  auto* design = ElaborateSrc(
      "module t;\n"
      "  logic [7:0] x;\n"
      "  initial begin\n"
      "    x = 8'd5;\n"
      "    assert(0);\n"
      "    x = 8'd10;\n"
      "  end\n"
      "endmodule\n",
      f);
  ASSERT_NE(design, nullptr);
  Lowerer lowerer(f.ctx, f.arena, f.diag);
  lowerer.Lower(design);
  f.scheduler.Run();
  // Execution continues past the failed assertion.
  auto* var = f.ctx.FindVariable("x");
  ASSERT_NE(var, nullptr);
  EXPECT_EQ(var->value.ToUint64(), 10u);
  // Default $error is emitted; failure counter incremented.
  EXPECT_EQ(f.ctx.AssertionFailCount(), 1);
}

